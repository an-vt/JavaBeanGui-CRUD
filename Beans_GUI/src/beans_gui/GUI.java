    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //button view all
        //hien thi len table
        DefaultTableModel dtm=(DefaultTableModel) tab.getModel();
        Object[] record=new Object[3];
        view1.setSql("select * from student");
        try {
            ResultSet rs=view1.view_record();
            while (rs.next()) {                
                record[0] =rs.getInt("id");
                record[1] =rs.getString("name");
                record[2] =rs.getString("email");
                dtm.addRow(record);
            }
            jLabel5.setText("hien thi len bang thanh cong!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

