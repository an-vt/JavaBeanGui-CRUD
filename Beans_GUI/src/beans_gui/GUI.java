    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //button add
        String sql="insert into student(id,name,email) values(?,?,?)";//cau lenh sql
        String name1=name.getText().toString();
        String email1=email.getText().toString();
        String id1=id.getText();
        insert1.setName(name1);
        insert1.setEmail(email1);;
        insert1.setId(Integer.parseInt(id1));
        try {
            jLabel5.setText(insert1.insert_record(sql));
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //button search
        String id1=id.getText();
        //cau lenh sql
        String sql="select * from student where id="+Integer.parseInt(id1);
        view1.setSql(sql);
        try {
            ResultSet rs=view1.view_record();
            //tro den hang tiep theo trong bang cua co so du lieu
            rs.next();
            name.setText(rs.getString("name"));
            email.setText(rs.getString("email"));
            jLabel5.setText("tim kiem thanh cong!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

